def draw_icon(oled, icon, width, x, y):
    for row, bits in enumerate(icon):
        for col in range(width):
            pixel = (bits >> (width - 1 - col)) & 1
            oled.pixel(x + col, y + row, pixel)

def add_icon(oled, icon, x, y):
    width = 10
    draw_icon(oled, icon, width, x, y)
    oled.show()
    
def make_icon_fb(icon, width):
    height = len(icon)
    buf = bytearray(((width + 7) // 8) * height)
    fb = framebuf.FrameBuffer(buf, width, height, framebuf.MONO_HLSB)
    for row, bits in enumerate(icon):
        for col in range(width):
            pixel = (bits >> (width - 1 - col)) & 1
            fb.pixel(col, row, pixel)
    return fb

